public class CodingHours {

        public static void main(String[] args) {

            int codingHours = 6 * 5 * 17;
            System.out.println("An average Green Fox attendee codes " + codingHours + " hours in a semester.");

            float workingHours = 52 * 17;
            float percentage = codingHours / workingHours * 100;
            System.out.println("The percentage of the coding hours in the semester is " + percentage + "%");

        }

}
