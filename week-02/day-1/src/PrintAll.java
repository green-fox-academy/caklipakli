public class PrintAll {
// - Create an array variable named `numbers`
//   with the following content: `[4, 5, 6, 7]`
// - Print all the elements of `numbers`

    public static void main(String[] args) {

        int[] t = {4, 5, 6, 7};
        for (int numbers : t) {
                 System.out.println(numbers);
        }
    
    }
    
}
